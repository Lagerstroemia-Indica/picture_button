/// PictureBubbleEffect enum class
///
/// this possible when PictureButton's [bubbleEffect] is true.
///
/// -
///
/// default is [shrink]
enum PictureBubbleEffect {
  /// effect animation to inside
  ///
  /// scale to 0.95
  shrink,

  /// effect animation to outside
  ///
  /// scale to 1.05
  expand,
}
